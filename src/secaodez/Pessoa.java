package secaodez;

public class Pessoa {
  private String nome, email;
  private int quarto;
public String getNome() {
	return nome;
}
public void setNome(String nome) {
	this.nome = nome;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
}
public int getQuarto() {
	return quarto;
}
public void setQuarto(int quarto) {
	this.quarto = quarto;
}
  
  
}
